public class Switch {
    public static void main(String[] args) {
        int SwitchValue = 2;

        switch (SwitchValue){
            case 1:
                System.out.println("Value is 1");
                break;

            case 2:
                System.out.println("Value is 2");
                break;

            default:
                System.out.println("Others");
        }
    }
}
